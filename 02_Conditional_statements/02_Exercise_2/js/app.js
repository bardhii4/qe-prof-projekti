let grade = 1;

switch (grade) {
	case 6:
		console.log('excellent');
		break;

	case 5:
		console.log('very good');
		break;

	case 4:
		console.log('good');
		break;

	case 3:
		console.log('sufficient');
		break;

	case 2:
		console.log('passing');
		break;

	case 1:
		console.log('fail');
		break;

	default:
		console.log("you didn't take the exam");
		break;
}
